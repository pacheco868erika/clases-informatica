#Lados de un triángulo y su perímetro
ladoA= float(input("Ingresa el lado A de un triángulo: "))
ladoB= float(input("Ingresa el lado B de un triángulo: "))
ladoC= float(input("Ingresa el lado C de un triángulo: "))
